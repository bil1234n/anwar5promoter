                <script>
                    function openVideoModal(el) {
                        var id = el.getAttribute('data-video-id');
                        if(!id) return;
                        var modal = document.getElementById('videoModal');
                        var frame = document.getElementById('videoFrame');

                        // embed youtube iframe with modestbranding
                        frame.innerHTML = '<iframe width="100%" height="100%" src="https://www.youtube.com/embed/' + id + '?autoplay=1&rel=0&modestbranding=1" frameborder="0" allow="autoplay; fullscreen; picture-in-picture" allowfullscreen></iframe>';

                        modal.style.display = 'flex';
                        // lock scroll on body
                        document.body.style.overflow = 'hidden';
                    }

                    function closeVideoModal(e) {
                        // allow click on backdrop OR click on close button
                        if(e && e.type === 'click') {
                        // if click target is the backdrop or the close button
                        if(e.target.id !== 'videoModal' && !e.target.classList.contains('modal-close')) {
                            // clicked inside modal content, ignore
                            return;
                        }
                        }
                        var modal = document.getElementById('videoModal');
                        var frame = document.getElementById('videoFrame');
                        frame.innerHTML = '';
                        modal.style.display = 'none';
                        document.body.style.overflow = '';
                    }

                    // close modal with ESC
                    document.addEventListener('keydown', function(e){
                        if(e.key === "Escape") {
                        var modal = document.getElementById('videoModal');
                        if(modal.style.display === 'flex') closeVideoModal();
                        }
                    });

                    // prevent clicks inside modal from closing (stop propagation)
                    document.querySelectorAll('.modal, .modal *').forEach(function(n){ n.addEventListener('click', function(evt){ evt.stopPropagation(); }) });
